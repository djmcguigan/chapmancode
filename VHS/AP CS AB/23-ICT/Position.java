import java.util.*;

public class Position{
    private int row;
    private int col;
    
    public Position(){
        row = 0;
        col = 0;
    }
    
    public Position(int r, int c){
        row = r;
        col = c;
    }
    
    public int getRow(){
        return row;
    }
    public int getCol(){
        return col;
    }
}